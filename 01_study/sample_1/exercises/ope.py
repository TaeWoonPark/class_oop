a = 6
b = 2

sum = a + b  # 和: summation
diff = a - b  # 差: difference
pro = a * b  # 積: product
quo_flo = a / b  # 商: quotient (浮動小数点)
quo_int = a // b  # 商: quotient (整数)
rem = a % b  # 余: remainder

print(f"{a} + {b} = {sum}")
print(f"{a} - {b} = {diff}")
print(f"{a} × {b} = {pro}")
print(f"{a} ÷ {b} = {quo_flo}")
print(f"{a} ÷ {b} = {quo_int}")
print(f"{a} ÷ {b} の余りは {rem}")
